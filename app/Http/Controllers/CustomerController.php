        $customer->address = $data['address'];
        $customer->district = $data['district'];
        $customer->conscious = $data['conscious'];
        $customer->postal_code = $data['postal_code'];
        $customer->country = $data['country'];
        $customer->id_user = 1;
        $customer->save();
        return $data;

    }

